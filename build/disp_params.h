#pragma once

// configurable parameters

#define DS_SCREEN_WIDTH 320
#define DS_SCREEN_HEIGHT 240
#define DS_SELECTED_MODE DP_DISPLAY_MODE_TEXT_AND_GRAPHICS

#define DS_CHAR_WIDTH 8
#define DS_CHAR_HEIGHT 8

#define DS_BYTES_PER_CHAR ((DS_CHAR_WIDTH + 7) / 8)

#define DS_CHARS_PER_LINE (DS_SCREEN_WIDTH / DS_CHAR_WIDTH)

#define DS_BYTES_PER_LINE (DS_CHARS_PER_LINE * DS_BYTES_PER_CHAR)

#define DS_CHAR_LINES (DS_SCREEN_HEIGHT / DS_CHAR_HEIGHT)


#define DS_TEXT_START_ADDRESS 0x0000

#define DS_GRAPHICS_START_ADDRESS (DS_TEXT_START_ADDRESS + (DS_BYTES_PER_CHAR * DS_CHARS_PER_LINE * DS_CHAR_LINES))



#define DS_LINE_TIMING_CORRECTION 4
